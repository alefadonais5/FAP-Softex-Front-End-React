// 35. Peça ao usuário para digitar a base e a altura de um triângulo. Em seguida, calcule e exiba a área do triângulo. 
let base2 = Number(prompt(`Qual a base do triângulo? `));
let altura2 = Number(prompt(`Qual a altura do triângulo? `));
let area1 = (base2*altura2)/2;

alert(`A área do triângulo é: ${area1}`);